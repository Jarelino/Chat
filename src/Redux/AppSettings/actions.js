export const CHANGE_APP_BG = 'CHANGE_APP_BG';
export const CHANGE_HEADER_BG = 'CHANGE_HEADER_BG';
export const CHANGE_TEXT_COLOR = 'CHANGE_TEXT_COLOR';

export const setAppBg = color => ({
  type: CHANGE_APP_BG,
  payload: color,
});

export const setHeaderBg = color => ({
  type: CHANGE_APP_BG,
  payload: color,
});

export const setTextColor = color => ({
  type: CHANGE_APP_BG,
  payload: color,
});
